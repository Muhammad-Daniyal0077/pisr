    <script>
function togglePanel(panelId) {
    // Get the selected panel body and its parent panel
    const panelBody = document.querySelector(#${panelId} .vc_tta-panel-body);
    const parentPanel = panelBody.parentNode;
    
    // Check if the panel is currently open
    const isActive = parentPanel.classList.contains('active-panel');

    // Hide all panels and remove the active class from each
    document.querySelectorAll('.vc_tta-panel-body').forEach(panel => {
        panel.style.display = 'none';
        panel.parentNode.classList.remove('active-panel');
    });

    // If the panel was not active, display it and add the active class
    if (!isActive) {
        panelBody.style.display = 'block';
        parentPanel.classList.add('active-panel');
    }
}
</script>